"""
== Igual: Verifica se um valor é igual ao outro
!= Diferente de: Verifica se um valor é diferente de outro
> Maior que: Verifica se um valor é maior que outro
>= Maior ou igual: Verifica se um valor é maior ou igual a outro
< Menor que: Verifica se um valor é menor que outro
<= Menor ou igual: Verifica se um valor é menor ou igual a outro

"""

numero1 = 5
numero2 = 10

if numero1 == numero2:
    print("Ambos os números são iguais")

if numero1 != numero2:
    print("Os números são diferentes")

if numero1 > numero2:
    print("Número 1 é maior que Número 2")

if numero1 >= numero2:
    print("Número 1 é maior ou Igual ao Número 2")

if numero1 < numero2:
    print("Número 1 é menor que Número 2")

if numero1 <= numero2:
    print("Número 1 é menor ou Igual ao Número 2")

