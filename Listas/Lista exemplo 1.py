#              0      1       2    3        4
listaTeste = ["A", "Banana", "C", "D", "Elefante"]

print(listaTeste[0]) #Imprimindo a posição 0 da lista
print(listaTeste[1]) #Imprimindo a posição 1 da lista
print(listaTeste[2]) #Imprimindo a posição 2 da lista
print(listaTeste[3]) #Imprimindo a posição 3 da lista
print(listaTeste[4]) #Imprimindo a posição 4 da lista

print(listaTeste[-1]) #Último item da lista
print(listaTeste[1:3])
print(listaTeste[:3]) #Imprimo os 3 primeiros
print(listaTeste[2:]) #A partir do terceiro item
print(listaTeste[::2]) #Imprime de 2 em 2
print(listaTeste[::-1]) #Imprimindo a lista de trás para frente