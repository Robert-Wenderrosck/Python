"""
Operador
== Igual
!= Diferente De
> Maior que
< Menor que
>= Maior ou igual que
<= Menor ou igual que
"""

letra = "a"

#Operador = Igual
if letra == "a":
    print("A letra é igual a letra A")

#Operador != Diferente De
if letra != "a":
    print("A letra é diferente de A")

#Operador > Maior que
numero1 = 20
numero2 = 15

if numero1 > numero2:
    print("O número1 é maior que o número2")

#Outra forma de fazer
if numero1 > numero2: print("O número1 é maior que o número2")

#Operador < Menor que
n1 = 10
n2 = 30

if n1 < n2:
   print("O n2 é maior que o n1")

#Operador >= Maior ou igual que
num1 = 50


if num1 >= 20:
    print("O num1 é maior ou igual a 20")

num2 = 40
#Operador <= Menor ou igual que
if num2 <= 15:
    print("O num2 é menor ou igual a 15")